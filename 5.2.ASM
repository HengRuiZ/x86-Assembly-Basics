 CODE SEGMENT
 ASSUME CS:CODE
 CS0 EQU 3000H
 MY8255_A EQU CS0+00H ;8255 的A 口地址
 MY8255_B EQU CS0+01H ;8255 的B 口地址
 MY8255_C EQU CS0+02H ;8255 的C 口地址
 MY8255_MODE EQU CS0+03H ;8255 的控制寄存器地址
 START:MOV DX,MY8255_MODE
 MOV AL,10000000B
 OUT DX,AL
 LOOP1:MOV DX,MY8255_A
 MOV AL,00011000B
 OUT DX,AL
 MOV DX,MY8255_B
 MOV AL,10000001B
 OUT DX,AL
 CALL DELAY
 MOV DX,MY8255_A
 MOV AL,00100100B
 OUT DX,AL
 MOV DX,MY8255_B
 MOV AL,01000010B
 OUT DX,AL
 CALL DELAY
 MOV DX,MY8255_A
 MOV AL,01000010B
 OUT DX,AL
 MOV DX,MY8255_B
 MOV AL,00100100B
 OUT DX,AL
 CALL DELAY
 MOV DX,MY8255_A
 MOV AL,10000001B
 OUT DX,AL
 MOV DX,MY8255_B
 MOV AL,00011000B
 OUT DX,AL
 CALL DELAY
 LOOP LOOP1
 
 DELAY PROC NEAR
 MOV CX,4000H
 LOOP3:MOV BX,CX
 LOOP2:NOP
 LOOP LOOP2
 MOV CX,BX
 LOOP LOOP3
 RET
 DELAY ENDP
 
 
 
 CODE ENDS
 END START
 
 